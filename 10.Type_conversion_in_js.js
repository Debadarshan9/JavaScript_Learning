// Number to String
var x = 123;
console.log(typeof x)

String(x)
console.log(typeof x.toString())

// Date to String
var d = new Date()
console.log(d)
console.log(d.toString())
console.log(String(d))

// Boolean to String
var b = false
console.log(typeof b)
var s = String(b)
console.log(typeof s)

// Boolean to Number
var n = Number(b)
console.log(n)
console.log(typeof n)

